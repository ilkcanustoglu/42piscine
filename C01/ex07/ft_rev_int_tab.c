void	ft_rev_int_tab(int *tab, int SIZE)
{
	int	tab2[SIZE];
	int	i;

	i = 0;
	while (i < SIZE)
	{
		tab2[i] = *(tab + (SIZE - 1 - i));
		i++;
	}
	i = 0;
	while (i < SIZE)
	{
		tab[i] = tab2[i];
	i++;
	}
}
